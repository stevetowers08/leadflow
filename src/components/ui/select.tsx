      'min-h-[44px] py-2 sm:py-1.5 pl-8 pr-2',
      'text-sm',
      'outline-none focus:bg-accent focus:text-accent-foreground',
      'data-[disabled]:pointer-events-none data-[disabled]:opacity-50',
